package main.dayfive;





public class Math {
    public static void main(String[] args) {

        //The Math.max(x, y) method can be used to find the highest value of x and y:
        java.lang.Math.max(8.6, 5.2);
        System.out.println(java.lang.Math.max(8.6, 5.2));

        //The Math.min(x, y) method can be used to find the lowest value of x and y:
        java.lang.Math.min(8.6, 5.2);
        System.out.println(java.lang.Math.min(8.6, 5.2));

        //The Math.abs(x) method returns the absolute (positive) value of x:
        java.lang.Math.abs(-4.7);
        System.out.println(java.lang.Math.abs(-4.7));

        //The Math.ceil(x) method returns the value of x rounded up to its nearest integer:
        java.lang.Math.ceil(7.4);
        System.out.println(java.lang.Math.ceil(7.4));

        //The Math.floor(x) method returns the value of x rounded down to its nearest integer:
        java.lang.Math.floor(7.8);
        System.out.println(java.lang.Math.floor(7.8));

        //The Math.pow(x, y) method returns the value of x to the power of y:
        java.lang.Math.pow(5, 3);
        System.out.println(java.lang.Math.pow(5, 3));

        //The Math.sqrt(x) method returns the square root of x:
        java.lang.Math.sqrt(64);
        System.out.println(java.lang.Math.sqrt(64));


    }
}