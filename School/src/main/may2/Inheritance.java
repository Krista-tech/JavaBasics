/**
 *
 * 💡 What is Inheritance?
 * Inheritance is like sharing cool stuff from one class to another.
 *
 * Imagine your class is a superhero parent (the example, most important class), and other classes are the superhero kids.
 * The kids get powers from the parent class — like speed, strength, or a special voice.
 *
 * We call the parent class the "superclass" and the child class the "subclass".
 *
 *
 * 👩‍👧 Real-Life Analogy:
 * Your mom or dad can cook and drive. You learn from them and now you can cook and drive too!
 * You still have your own unique things (like skateboarding or painting), but you also inherited what they had!
 *
 * 🧬 Key Java Words:
 * extends: this is how a class inherits another class
 * The child class gets fields and methods from the parent
 * The child can also change them (this is called overriding – we’ll do that later!)
 *
 */