package main.day2.outputandcomments;

public class Output {
    public static void main(String[] args) {


        /**
         * You can use the println() method to output values or print text in Java
         * Text must be wrapped inside double quotations marks ""
         */

        System.out.println("Hello World");

        /** You can add as many println() methods as you want. */

        System.out.println("Hello World");
        System.out.println("Hello World2");
        System.out.println("Hello World3");

        /**
         * There is also a print() method, which is similar to println().
         * Note that we add an extra space after the first word in the example for better readability.
         * The only difference is that it does not insert a new line at the end of the output:
         */

        System.out.print("Hello ");
        System.out.print("I will print on the same line.");

    }
}
