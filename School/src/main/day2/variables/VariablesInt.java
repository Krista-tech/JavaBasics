package main.day2.variables;

public class VariablesInt {
    public static void main(String[] args) {


        /**
         * int
         * is a type of number that doesn't have a decimal point.
         * It's used to store whole numbers, like 5, 100, or -20.
         * You can think of it as a number you can count with,
         * like how many apples you have or how many pages are in a book.
         */

        /**
         * You can declare an int and assign a value to it at the same time.
         */
        int myNum = 14;
        System.out.println(myNum);

        /**
         * You can also declare an int without assigning a value to it.
         * In this case, the value will be 0.
         */
        int myNum2;
        myNum2 = 10;
        System.out.println(myNum2);
    }
}